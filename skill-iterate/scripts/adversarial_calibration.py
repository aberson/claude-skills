"""Adversarial-mutation calibration for the per-skill scoring grader.

Periodic (pre-flight or weekly) calibration that injects a small set of known
defects into a target SKILL.md, scores each defective copy against the current
grader, and computes the fraction of mutations that produced a score drop
relative to the baseline (the "catch rate"). A grader whose catch rate is
below the configured threshold cannot discriminate defective from baseline on
this skill, and the calling /skill-iterate Phase 0 pre-flight should defect-park
the skill via the :data:`GRADER_BROKEN_CRASH_CLASS` sub-class rather than enter
the iteration loop and burn budget against a broken signal.

The mechanism is the runtime/grader-targeted counterpart to the storage-time
golden-bad verification gate in ``generate_bad_examples.py`` — see
``docs/investigations/skill-iterate-hill-climbing/06-adversarial-mutation-grader-discrimination-tests.md``
for the design rationale. The mutations land in this module rather than being
generated by an LLM so calibration is deterministic given a seed and stays
cheap (no sub-agent dispatch needed for the mutation step itself).

CLI:
    python adversarial_calibration.py <skill-name>
    python adversarial_calibration.py --skill <skill-name>
    python adversarial_calibration.py --fleet [--batch-size N]
    python adversarial_calibration.py --num-mutations N
    python adversarial_calibration.py --threshold 0.70
    python adversarial_calibration.py --seed <int>

JSON summary is written to stdout (machine-consumed by orchestrators);
human-readable progress is written to stderr.

Pure-Python / stdlib only; the scoring function is dependency-injected so unit
tests do not require a live LLM grader and the CLI can be wired to either the
structural-only fallback or the full composite procedure depending on operator
preference.
"""

