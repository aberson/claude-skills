GRADER_BROKEN_CRASH_CLASS: str = "grader-broken-for-skill"

# Default fraction of mutations the grader must "catch" (score strictly lower
# than baseline) for calibration to pass. Per plan §5 New Components.
DEFAULT_CATCH_RATE_THRESHOLD: float = 0.70

# Default number of mutations to enumerate per calibration run. Investigation
# file 06 §How to apply says 3-5 covers the major defect-type axes; we pick 10
